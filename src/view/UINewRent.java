    public void completeClientName(){
        connect.Connection();
        
        connect.executaSql("SELECT * FROM tb_clients ORDER BY client_name");
        try {
            connect.rs.first();
            cb_client_name.removeAllItems();
            do {
                cb_client_name.addItem(connect.rs.getString("client_name"));
            } while (connect.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher clientes: "+ ex);
        }
    }
    
