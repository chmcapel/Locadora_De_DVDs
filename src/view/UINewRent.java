    private void bt_rentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_rentActionPerformed
       if (txt_rent_date.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Insira a data de locação para continuar!");
            txt_rent_date.requestFocus();

        } else if (txt_rent_delivery_date.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Insira a data de devolução para continuar!");
            txt_rent_delivery_date.requestFocus();

        } else {

            rentModel.setClient_name((String) cb_client_name.getSelectedItem());
            rentModel.setProduct_name((String) cb_product_name.getSelectedItem());
            rentModel.setRent_date(txt_rent_date.getText());
            rentModel.setRent_delivery_date(txt_rent_delivery_date.getText());
            DAORent.Salvar(rentModel);
            cb_client_name.setSelectedIndex(0);
            cb_product_name.setSelectedIndex(0);
            txt_rent_date.setText("");
            txt_rent_delivery_date.setText("");
            dispose();
            connect.Disconnect();
            
        }
    }//GEN-LAST:event_bt_rentActionPerformed

