    private void fillTable(String Sql){
        ArrayList dados = new ArrayList();
        String [] columns = new String []{"Nome","CPF","Endereço","Número","Telefone"};
        connect.Connection();
        connect.executaSql(Sql);
        
        try {
            
            connect.rs.first();
            do {
                
                dados.add(new Object[]{connect.rs.getString("client_name"),connect.rs.getString("client_cpf")
                        ,connect.rs.getString("client_address"),connect.rs.getString("client_address_number"),connect.rs.getString("client_phone")});
                
            }while(connect.rs.next());                    
        } catch(SQLException ex) {
            
        }
        TableModel model = new TableModel(dados, columns);
        
        tb_reports.setModel(model);
        tb_reports.getColumnModel().getColumn(0).setPreferredWidth(500);
        tb_reports.getColumnModel().getColumn(0).setResizable(false);
        tb_reports.getColumnModel().getColumn(1).setPreferredWidth(400);
        tb_reports.getColumnModel().getColumn(1).setResizable(false);
        tb_reports.getColumnModel().getColumn(2).setPreferredWidth(500);
        tb_reports.getColumnModel().getColumn(2).setResizable(false);
        tb_reports.getColumnModel().getColumn(3).setPreferredWidth(200);
        tb_reports.getColumnModel().getColumn(3).setResizable(false);
        tb_reports.getColumnModel().getColumn(4).setPreferredWidth(400);
        tb_reports.getColumnModel().getColumn(4).setResizable(false);
        tb_reports.getTableHeader().setReorderingAllowed(false);
        tb_reports.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        connect.Disconnect();
    }

